# Slicing Strings

b = "Hello, World!"
print(b[2:5])

#llo

b = "Hello, World!"
print(b[:5])

#Hello

b = "Hello, World!"
print(b[2:])

#llo, World!

b = "Hello, World!"
print(b[-5:-2])

#orl